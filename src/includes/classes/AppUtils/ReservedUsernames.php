    /**
     * Is username reserved?
     *
     * @since 15xxxx Reserved usernames.
     *
     * @param string $username Username to check.
     *
     * @return bool True if username is reserved.
     */
    public function isReserved(string $username): bool
    {
        $username = mb_strtolower($username);
        $username = str_replace(['-', '_'], '', $username);

        if (!is_null($is = &$this->cacheKey(__FUNCTION__, $username))) {
            return $is; // Cached this already.
        }
        if (mb_strlen($username) < 3) {
            return ($is = true);
        }
        if (in_array($username, $this->reserved_strings, true)) {
            return ($is = true);
        }
        foreach ($this->reserved_patterns as $_regex) {
            if (preg_match($_regex, $username)) {
                return ($is = true);
            }
        } // unset($_regex); // Housekeeping.

        return ($is = false);
    }

